package naumen.project.entity.enums;

/**
 * Енам с типами ролей для авторизованных пользователей
 */
public enum Role {
    /**
     * Роль клиента. Человек, который пользуется сервисом доставки
     */
    CLIENT,

    /**
     * Администратор сервиса доставки
     */
    ADMIN,

    /**
     * Ресторан. Продает блюда клиентам - пользователям
     */
    RESTAURANT,

    /**
     * Курьер, который доставляет заказы
     */
    COURIER,
}
